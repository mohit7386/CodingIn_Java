import java.util.*;

public class Code2 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int sum;
        System.out.println("Enter the value of a");
        int a = sc.nextInt();
        System.out.println("Enter the value of b");
        int b = sc.nextInt();
        sum = a + b;
        System.out.println("The sum of a and b is:- " + sum);
        sc.close();
    }
}
